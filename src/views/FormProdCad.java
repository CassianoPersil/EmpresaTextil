    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        try {
            Produto p = new Produto();
            ProdModel pm = new ProdModel();

            p.setNome(ctNome.getText());
            p.setQtde(parseInt(ctQuant.getText()));
            p.setUn(ctUnid.getText());
            p.setValor(parseDouble(cfValor.getText()));
            pm.salvar(p);
            JOptionPane.showMessageDialog(rootPane, "Produto cadastrado com sucesso!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao salvar Produto!");
        }
    }//GEN-LAST:event_btSalvarActionPerformed

