    private void listarFuncionarios() {
        tbListaFun.setModel(new DefaultTableModel(
                new Object[][]{}, new String[]{"Código", "Nome", "Data Cadastro", "CPF", "Telefone"}));
        DefaultTableModel tabela = (DefaultTableModel) tbListaFun.getModel();
        try {
            for (Funcionario funcionario : f) {
                tabela.addRow(new Object[]{funcionario.getId(),
                    funcionario.getNome(),
                    df.format(funcionario.getData()),
                    funcionario.getCpf(),
                    funcionario.getTelefone()});
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Erro na listagem de funcionários!");
        }
    }

