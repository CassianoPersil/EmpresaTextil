    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        try {
            FormFunCad fcad = new FormFunCad();
            fcad.situacao = "alterar";
            fcad.f.setId((Long) tbListaFun.getValueAt(tbListaFun.getSelectedRow(), 0));
            fcad.f.setNome((String) tbListaFun.getValueAt(tbListaFun.getSelectedRow(), 1));
            fcad.f.setData(df.parse((String) tbListaFun.getValueAt(tbListaFun.getSelectedRow(), 2)));
            fcad.f.setCpf((String) tbListaFun.getValueAt(tbListaFun.getSelectedRow(), 3));
            fcad.f.setTelefone((String) tbListaFun.getValueAt(tbListaFun.getSelectedRow(), 4));
            for (int i = 0; i < tbFilhos.getRowCount(); i++) {
                fcad.filhos.add(new Filho(
                        (Long)tbFilhos.getValueAt(i, 0), 
                        (String)tbFilhos.getValueAt(i, 1), 
                        null));
            }
            fcad.setTitle("Alteração de Cadastro");
            fcad.setModal(true);
            fcad.setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Selecione um funcionário");
        }

    }//GEN-LAST:event_btAlterarActionPerformed

