    private void listarFilhos() {
        limparFilhos();
        FunModel fm = new FunModel();
        Funcionario f = fm.listaUnicoFun((Long) tbListaFun.getValueAt(tbListaFun.getSelectedRow(), 0));
        for (Filho filho : f.getFilhos()) {
            tabelaFilhos.addRow(new Object[]{filho.getId(), filho.getNome()});
        }
        //tbFilhos.setModel(new DefaultTableModel(
        // new Object[][]{}, new String[]{"Coódigo", "Nome"}));
    }

