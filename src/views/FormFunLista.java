    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        // FunModel fmo = new FunModel();
        if (tbListaFun.getSelectedRow() < 0) {
            JOptionPane.showInputDialog(rootPane, "Selecione uma linha da tabela");
        } else {
            try {
                if (JOptionPane.showConfirmDialog(rootPane, "Confirma exlusão do Registro?") == 0) {
                    new FunModel().remover((Long) tbListaFun.getValueAt(tbListaFun.getSelectedRow(), 0));
                    JOptionPane.showMessageDialog(rootPane, "Registro removido");
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Erro ao excluir registro");
            }
        }
    }//GEN-LAST:event_btExcluirActionPerformed

